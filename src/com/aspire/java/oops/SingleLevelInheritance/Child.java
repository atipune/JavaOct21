package com.aspire.java.oops.SingleLevelInheritance;

public class Child extends Parent 
{
	public void iphone()
	{
		System.out.println("Iphone from child class");
	}
	public void bike()
	{
		System.out.println("Bike from child class");
	}
	public void money()		//same method available in parent class
	{
		System.out.println("money from child class");
	}
}
