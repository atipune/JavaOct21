package com.aspire.java.constructor;

public class Demo3  //UserDefinedConstructor
{
    int i;
    String j;
    float k;
    char l;
    Demo3()     //Userdefined constructor
    {
        i=10;
        j="Ajinkya";
        k=3.14f;
        l='A';
    }

    public void method3()
    {
        System.out.println(i);
        System.out.println(j);
        System.out.println(k);
        System.out.println(l);
    }

}
