package com.aspire.java.basics.VariablesDataTypes;

public class Datatypes3 
{
	public static void main(String[] args) {
		byte a=2;	//1. & 2. variable declaration and initialization at same location
		byte b;		//1. Variable declare
		b=3;		//2. variable initialize 
		int sum;
		sum=a+b;
		System.out.println(a+b); 	//3. Usage
		System.out.println(sum);
		float c= 2.27f;
		float d;
		d=a+c;
		System.out.println(d);
		
	}
}
