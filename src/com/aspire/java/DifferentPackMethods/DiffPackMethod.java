package com.aspire.java.DifferentPackMethods;

public class DiffPackMethod 
{
	public static void method5()
	{
		System.out.println("Running static method-5 from different package");
	}
	public static void method6()
	{
		System.out.println("Running static method-6 from different package");
	}
	public static void method2()
	{
		System.out.println("Running static method-2 from different package");
	}
	

	public void method12()
	{
		System.out.println("Running non static method-12 from different package");
	}
	public void method15()
	{
		System.out.println("Running non static method-15 from different package");
	}
	public void method16()
	{
		System.out.println("Running non static method-16 from different package");
	}
}
